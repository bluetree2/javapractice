package ch04.lecture;

public class C01If {
    public static void main(String[] args) {
        //  if : 조건문(분기문, branch, conditional)
        // 조건이 true이면 실행
        // 조건이 false이면 실행하지 않음

        if (true) {
            // 조건이 true일 때 실행
            System.out.println("실행되었습니다.");
        }

        if (false) {
            // 조건이 false일 때 실행
            System.out.println("실행되지 않았습니다.");
        }

        // truthy, falsy 없음
        
    }
}
