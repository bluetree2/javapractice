package ch04.lecture;

public class C02Else {
    public static void main(String[] args) {
        // else : 조건이 false일 때 실행

        if (true) {
            // 조건이 true일 때 실행
            System.out.println("실행되었습니다.");
        } else {
            // 조건이 false일 때 실행
            System.out.println("실행되지 않았습니다.");
        }

        if (false) {
            // 조건이 true일 때 실행
            System.out.println("실행되었습니다.");
        } else {
            // 조건이 false일 때 실행
            System.out.println("실행되지 않았습니다.");
        }   
    }
}
