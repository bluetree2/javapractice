package ch01.lecture;

public class C03Print {
    public static void main(String[] args) {
        //sout
        System.out.println("hi");

        //ln : 새로운 줄
        System.out.printf("hello\njava\n");

        // \t : 탭
        System.out.printf("hello\tjava\n");

        // \\ : 역슬래시
        System.out.printf("hello\\java\n");

        // \" : 큰따옴표
        System.out.printf("hello\"java\n");

        // \' : 작은따옴표
        System.out.printf("hello\'java\n");

        // \r : 캐리지 리턴
        System.out.printf("hello\rjava\n");

        // \f : 폼피드
        System.out.printf("hello\fjava\n");
        
        

    }
}
