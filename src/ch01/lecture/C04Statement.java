package ch01.lecture;

public class C04Statement {

    public static void main(String[] args) {
        // 명령문
        System.out.println("출력문1");
        // 순서대로 실행됨
        System.out.println("출력문2");
        // ;으로 끝나야 함
        System.out.println("출력문3");

        // 명령문 여러줄 작성 가능
        System.out.println("출력문4");
        System.out.println("출력문5");
        // ctrl + shift + enter : 나머지 문장 자동 완성
        System.out.println("출력문6");
        // ctrl + alt + l : 코드 format
        // ctrl + enter : 줄 분리
        // ctrl + / : 주석 추가/제거
        // ctrl + shift + / : 여러줄 주석 추가/제거
        // ctrl + shift + u : 대소문자 전환
        // ctrl + shift + o : 자동 import
        // ctrl + space : 자동완성
        // ctrl + alt + 화살표(위,아래) : 줄 복사
        // ctrl + d : 현재 커서의 한줄 아래에 복사
        System.out.println("출력문7");

        //alt shift 위아래 : 코드 위아래로 이동
        System.out.println("출력문0");


        //ctrl alt enter : 윗 줄에 코드 입력



    }
}
